void setDir( float lspeed, float rspeed) { // timer 4 banh trai timer 2 banh phai
  if(lspeed < 0) {
	  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, 0);
	  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_2, -lspeed);
  } else if(lspeed > 0){
	  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, lspeed);
	  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_2, 0);
  } else {
	  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_1, 0);
	  __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_2, 0);
  }
  if(rspeed < 0) {
	  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, 0);
	  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, -rspeed);
  } else if(rspeed > 0){
	  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, rspeed);
	  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, 0);
  } else {
	  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, 0);
	  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, 0);
  }
}
