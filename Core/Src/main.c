void pid_left(float Kp, float Ki, float Kd) {
  past_errors(error);
  int sum = errors_integral(5);
  error = 3200 - sensor[1];
  P = Kp * error;
  I = Ki * sum;
  D = Kd * (error - last_error);
  float speedChange = P + D + I;
  last_error = error;
  lspeed = basespeed - speedChange;
  rspeed = basespeed + speedChange;
  if(lspeed > 250) lspeed = 249;
  if(rspeed > 250) rspeed = 249;
  if(lspeed < -250) lspeed = -249;
  if(rspeed < -250) rspeed = -249;
  if(!(sensor[1] < 2100 || (sensor[1] < 2100 && sensor[0] > 3000) || (sensor[1] < 2100 && sensor[2] > 3000)) &&
     !((sensor[1] > 2900 && sensor[0] > 3000) || (sensor[1] > 2900 && sensor[2] > 3000))) {
      setDir(lspeed, rspeed);
  } else {
      turn();
  }
}

